  def slug_for(genre)
    mapped = SLUG_BY_DISPLAY_NAME[genre.name]
    base = if mapped.present?
             mapped
           else
             parameterized = I18n.with_locale(:uk) { genre.name.to_s.parameterize }
             parameterized.presence || "genre-#{genre.id}"
           end
    uniquify_slug(genre, base)
  end

  def uniquify_slug(genre, base)
    candidate = base.to_s
    n = 0
    while Genre.unscoped.where.not(id: genre.id).exists?(slug: candidate)
      n += 1
      candidate = "#{base}-#{n}"
    end
    candidate
  end

